print("Bienvenido/a al sistema de Promedio")
a = float(input("Ingrese la primera nota: "))
b = float(input("Ingrese la segunda nota: "))
c = float(input("Ingrese la tercera nota: "))

promedio = (a + b + c) / 3

print("------------------------------")
print(f"El promedio de las notas es: {promedio:.2f}")